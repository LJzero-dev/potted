package vo;

public class SalesSlip {
	// 월 별 매출전표 관련
	private int ss_month, ss_sale, ss_real;
	private String ss_pcb;

	public int getSs_month() {
		return ss_month;
	}

	public void setSs_month(int ss_month) {
		this.ss_month = ss_month;
	}

	public int getSs_sale() {
		return ss_sale;
	}

	public void setSs_sale(int ss_sale) {
		this.ss_sale = ss_sale;
	}

	public int getSs_real() {
		return ss_real;
	}

	public void setSs_real(int ss_real) {
		this.ss_real = ss_real;
	}

	public String getSs_pcb() {
		return ss_pcb;
	}

	public void setSs_pcb(String ss_pcb) {
		this.ss_pcb = ss_pcb;
	}

	
	
}
