package vo;

public class GardenInfo {
	private int gi_idx;
	private String gi_name, gi_location, gi_link, gi_content, gi_date;
	public int getGi_idx() {
		return gi_idx;
	}
	public void setGi_idx(int gi_idx) {
		this.gi_idx = gi_idx;
	}
	public String getGi_name() {
		return gi_name;
	}
	public void setGi_name(String gi_name) {
		this.gi_name = gi_name;
	}
	public String getGi_location() {
		return gi_location;
	}
	public void setGi_location(String gi_location) {
		this.gi_location = gi_location;
	}
	public String getGi_link() {
		return gi_link;
	}
	public void setGi_link(String gi_link) {
		this.gi_link = gi_link;
	}
	public String getGi_content() {
		return gi_content;
	}
	public void setGi_content(String gi_content) {
		this.gi_content = gi_content;
	}
	public String getGi_date() {
		return gi_date;
	}
	public void setGi_date(String gi_date) {
		this.gi_date = gi_date;
	}
	
	
}
